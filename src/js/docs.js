let documentations = document.querySelector(".documents");
let popup = document.querySelector(".popup");
let popup_close = document.querySelector(".popup__close");

popup_close.onclick = () => {
    popup.style.opacity = 0;
    popup.style.visibility = "hidden";
}

for (let doc of docs) {
    doc.title_truncated = doc.title.length > 18 
        ? doc.title.slice(0,15) + "..."
        : doc.title;

    let document_el = document.createElement("div");

    document_el.className="documents__document";
    document_el.innerHTML=`
        <img src="${doc.image}" width="200px" height="300px">
        <div>
            <p>${doc.title_truncated}</p>
        </div>
        `;

    document_el.addEventListener('click', () => {
        let popup_text = document.querySelector('.popup__text');
        let popup_title = document.querySelector('.popup__title');
        popup_title.innerText = doc.title;
        popup_text.innerHTML = doc.text;
        popup.style.visibility = "visible";
        popup.style.opacity = 1;
    });
    
    documentations.appendChild(document_el);
}